package com.apipothi.switchs;

public class Lab10 {
    public static void main(String[] args) {

        System.out.println("***** STRAT- Lab8 ***** ");

        // Switch

        String myYoutube="APIPOTHI";
        
        switch (myYoutube){

            case "API":
                System.out.println("Case-1-API");
                break;

            case "APIPOTHI":
                System.out.println("Case-2-APIPOTHI");
                break;

            case "POTHI":
                System.out.println("Case-10-POTHI");
                break;

            default:
                System.out.println("I am Default");
                break;

        }

        System.out.println("***** END- Lab8 ***** ");

    }
}
