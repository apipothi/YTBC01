package com.apipothi.switchs;

public class Lab3 {
    public static void main(String[] args) {

        System.out.println("***** STRAT- Lab2 ***** ");

        // Switch

        int a = 1;
        
        switch (a){

            case 1:
                System.out.println("Case-1");
                break;
            case 2:
                System.out.println("Case-2");
                break;
            case 10:
                System.out.println("Case-10");
                break;
        }

        System.out.println("***** END- Lab2 ***** ");

    }
}
