package com.apipothi.switchs;

public class Lab7 {
    public static void main(String[] args) {

        System.out.println("***** STRAT- Lab7 ***** ");

        // Switch

        int a = 2;
        
        switch (a){

            case 1:
                System.out.println("Case-1");

            case 2:
                System.out.println("Case-2");

            case 10:
                System.out.println("Case-10");

            default:
                System.out.println("I am Default");

        }

        System.out.println("***** END- Lab7 ***** ");

    }
}
