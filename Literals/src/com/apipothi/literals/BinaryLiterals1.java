package com.apipothi.literals;

public class BinaryLiterals1 {

    public static void main(String[] args) {

        System.out.println("*********** START - Literals Example BinaryLiterals1 ::  ");

        int x = 0b111;

        System.out.println(" x:: "+ x); // x:: 7 (111)₂ = (1 × 2²) + (1 × 2¹) + (1 × 2⁰) = (7)₁₀

        System.out.println("*********** END - Literals Example BinaryLiterals1 ::  ");
    }
}
