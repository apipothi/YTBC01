package com.apipothi.literals;

public class BooleanLiterals1 {

    public static void main(String[] args) {

        System.out.println("*********** START - Literals Example BooleanLiterals1 ::  ");

        boolean x = true;

        System.out.println(" x:: "+ x); // true

        boolean x1 = false;

        System.out.println(" x:: "+ x1); // true

        System.out.println("*********** END - Literals Example BooleanLiterals1 ::  ");
    }
}
