package com.apipothi.literals;

public class FloatLiterals1 {

    public static void main(String[] args) {

        System.out.println("*********** START - Literals Example OctalLiterals1 ::  ");

        float x = 3.1414f;

        System.out.println(" x:: "+ x); //x:: 3.1414

        float x1 = 3.1414F;
        System.out.println(" x1:: "+ x1); //x:: 3.1414

        System.out.println("*********** END - Literals Example OctalLiterals1 ::  ");
    }
}
