package com.apipothi.literals;

public class HexaDecimalLiterals {

    public static void main(String[] args) {

        System.out.println("*********** START - Literals Example HexaDecimalLiterals ::  ");

        //int x = 0X10;

        int x = 0x10;

        System.out.println(" x:: "+ x); // x:: 16 :: (10)₁₆ = (1 × 16¹) + (0 × 16⁰) = (16)₁₀

        System.out.println("*********** END - Literals Example HexaDecimalLiterals ::  ");
    }
}
