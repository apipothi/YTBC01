package com.apipothi.literals;

public class OctalLiterals1 {

    public static void main(String[] args) {

        System.out.println("*********** START - Literals Example OctalLiterals1 ::  ");

        int x = 010;

        System.out.println(" x:: "+ x); // x:: 8 // Conversion from octal to Decimal : 010 = (0 × 8²) + (1 × 8¹) + (0 × 8⁰) = 8

        System.out.println("*********** END - Literals Example OctalLiterals1 ::  ");
    }
}
