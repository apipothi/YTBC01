package com.apipothi.literals;

public class StringLiterals1 {

    public static void main(String[] args) {

        System.out.println("*********** START - Literals Example StringLiterals1 ::  ");

        String x = "APIPOTHI";
        System.out.println(" x:: "+ x); // x:: APIPOTHI

        String x1 = "12345";
        System.out.println(" x:: "+ x1); // x:: 12345

        System.out.println("*********** END - Literals Example StringLiterals1 ::  ");
    }
}
