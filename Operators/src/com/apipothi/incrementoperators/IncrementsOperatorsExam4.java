package com.apipothi.incrementoperators;
public class IncrementsOperatorsExam4 {
    public static void main(String[] args) {

        System.out.println("******* START-IncrementsOperatorsExam2 *******");

        int a3 = 20;  // Pre Increment
        int a4 = ++a3;// a4 is the int type variable which is reciving the
        // pre incremented value form a3
        System.out.println("---- Value of A3 :: "+a3);// 21
        System.out.println("---- Value of A4 :: "+a4);// 21

        System.out.println("******* END-IncrementsOperatorsExam2 *******");
    }
}
