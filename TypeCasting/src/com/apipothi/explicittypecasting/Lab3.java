package com.apipothi.explicittypecasting;

public class Lab3 {
    public static void main(String[] args) {

        System.out.println("*** STRAT -- Lab2 ******* ");

        float f1 = 160.25F;
        int i1 = (int)f1;// I am type casting from integer to Short

        System.out.println("*** Value of f1 :: ******* "+f1);// *** Value of f1 :: ******* 160.25
        System.out.println("*** Value of i1 :: ******* "+i1);// *** Value of i1 :: ******* 160

        System.out.println("*** END -- Lab2 ******* ");
    }
}
